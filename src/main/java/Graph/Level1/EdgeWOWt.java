package Graph.Level1;

public class EdgeWOWt {

    int src;
    int nbr;

    public EdgeWOWt(int src, int nbr) {
        this.src = src;
        this.nbr = nbr;
    }

    public void setSrc(int src) {
        this.src = src;
    }

    public void setNbr(int nbr) {
        this.nbr = nbr;
    }

    public int getSrc() {
        return src;
    }

    public int getNbr() {
        return nbr;
    }
}
